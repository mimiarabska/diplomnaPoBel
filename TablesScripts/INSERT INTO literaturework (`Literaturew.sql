ЙОВЧО. Печо, недей така, рода сме!…
ПЕЧО. Кой, бе? С тебе ли съм рода, консуматор с консуматорите!!! Перушинен цар!…
Двайсет хиляди пилета гледа в двора си! Четири хиляди левчета на седмица само от
яйца!…
БАЙ ЦОНЧО. Печо!…
ПЕЧО. И какво?… Как не купи една книга, бе, как не помисли за нещо духовно… Събира,
трупа, къщата му пълна, ама главата му празна!… Къде ще вървим ние с тези консуматори
бе, бай Цончо?
БАЙ ЦОНЧО. Печо, спри се малко, бе!…
ЙОВЧО. Този не е от нашия род! Този е сменен! В родилното е сменен!
ПЕЧО. Тъпкан е с пари!… А ние, духовните водачи на нацията, нямаме пукнат лев!…
Къде отиваме бе, бай Цончо?
ЙОВЧО. Бай Цончо, кажи му, иначе ще го прасна с шишето!… Може ли сега тука, пред
толкова народ… Говорят ли се такива работи… Тези от стопанската също ходят на
театър…
ПЕЧО. Щото не е верно, нали?
БАЙ ЦОНЧО (въздъхва). Донесете пръчките!… (Тишина, споглеждат се. Донасят
пръчките.)
ПЕЧО. Като кажеш истината – веднага пръчки!…
ДИРЕКТОР. Моля ви се, бой с пръчки в поверения ми театър не разрешавам. Да сме
наясно.
БАЙ ЦОНЧО. Ти не се меси. Дай!… (Подават му една пръчка, той я опитва.) Печо!… Ела.
(Печо неохотно идва.) Счупи я.
ПЕЧО. Бай Цончо…
БАЙ ЦОНЧО. Като ти казвам – счупи я. (Печо я счупва неохотно.) Йовчо…
ЙОВЧО (с готовност). Да.
БАЙ ЦОНЧО (вади друга пръчка). Счупи я ти. (Йовчо я счупва.)

Печо демонстративно потропва с крак, явно този ритуал му е добре известен и омръзнал.
Йовчо се старае с изплезен език.
БАЙ ЦОНЧО. Дайте снопа. (Дават му снопа пръчки.) Печо, счупи ги!… (Печо неохотно ги
взема, проформа се мъчи да ги счупи и му ги връща.)
ПЕЧО. Не мога.
БАЙ ЦОНЧО. Ха сега ти, Йовчо. (Подава му снопа.)
ЙОВЧО (мъчи се усърдно). И аз не мога.
БАЙ ЦОНЧО. А защо? А, Печо?
ПЕЧО. Щото са много.
БАЙ ЦОНЧО. Не са много, а са единни. Така и ние, нашият народ, трябва да сме единни.
Иначе ще ни чупят по един. Ха сега, целунете се!
Едва сдържайки отвращението си, и двамата се приближават и се прегръщат и целуват.
Печо си трие устата.
БАЙ ЦОНЧО. Така ви искам – единни и сплотени. Хайде, Йовчо!… (Излиза, сподирен от
Йовчо.)
КУМ (на Директора). Какво ще кажеш, а? Голям род сме ние!…
ПЕЧО (крещи). Не се издържа вечеее!… (Двамата го гледат.)
ДИРЕКТОР. Какво става? Какво се е случило?
ПЕЧО. Там е работата, че нищо не се е случило. Нищо не се случва. Стоим си до гуша в
перушина и чакаме нещо да се случи. И си попълваме списъка.
ДИРЕКТОР. Какъв списък?
ПЕЧО. Всяка нормална нация, като отвлече самолет, какви условия поставя? Да се
освободят там пет или шест души, нали?… А наш човек ако отвлече, знаеш ли какви ще са
му условията?
ДИРЕКТОР. Какви?
ПЕЧО. Да се вкарат в затвора поне десет души – колеги, приятели, началници… Всеки си
върви със списъка в джоба. И всяка седмица си го попълва с нови имена. Не че ще отвлече
самолет, то за тая работа кураж се иска, ама така, да не се мине, ако случайно се случи в
самолета… дето са го отвлекли ония… Да си го има той списъка и да се присъедини към
исканията!… Ние все към исканията се присъединяваме!… Другите действат, а ние се

присъединяваме… Онзиден чета във вестника – пак обиколили земното кълбо! Самотен
мореплавател! С една платноходка само! Обаче доказва величието на човешкия дух!… А
като го гледаш Йовчо, той какво ти доказва?… Ние гледаме пилета!…
ДИРЕКТОР. Вие нали не гледате пилета, защо не го обиколите?
ПЕЧО. Да ти кажа ли защо?
ДИРЕКТОР. Да, кажете…
ПЕЧО. Кога да го обиколя? Всеки ден съм на работа. Ти представяш ли си, отивам при
директора и му казвам: „Аз тръгвам да обикалям земното кълбо“. Че нали първото нещо,
което ще направи, след като ми премери температурата, ще бъде да ме попита по каква
линия? И аз какво ще отговоря – ами така, по частна, за величието на човешкия дух!… Че
нали изгарям прав, още там, в кабинета, до фикуса!… Кодексът ми дава два месеца – един
платен и един неплатен. Върви ти да обикаляш земното кълбо за два месеца. И то единият
от тях безплатен.
ДИРЕКТОР. Аз не искам. Става ми лошо на лодка.
КУМ. Остави сега човека!…
ПЕЧО. Но да речем, че напусна работа. Заради величието на човешкия дух. Оставя децата
си гладни. Какво мислиш, че ще стане?
ДИРЕКТОР. Не знам.
ПЕЧО. Няма да има платноходка. Платноходки няма под път и над път. С тях се
разкарват… разни… А ако по някакво чудо се намери платноход, то той ще е разбит, ще
трябва да се ремонтира, а знаеш как стават ремонтите у нас.
ДИРЕКТОР. За ремонтите си прав. Не върви нещо с ремонтите… Нашият театър откога
трябваше да влезе…
ПЕЧО. Ако потърсиш карта на океана с мусоните, пасатите и разните му голфстрими –
карта няма да има. А ако все пак има, тя ще е пълна с грешки. И ще има да се лутам между
Огнена земя и Нова Гвинея, докато ме изядат акулите. Или човекоядците. Или дивите
зверове. Все нещо ще ме изяде, няма да ми се размине, знам си аз. Е, при това положение
как да тръгна! Нормален човек тръгва ли? (Откъм сватбарите се чуват викове: „Печоо!
Печооо!…“.) Кара, ако до половин час не почнем, да знаеш – без мене! (Излиза.)
КУМ. А? Виждаш ли го?… Интелектуалец!…
Тука скача Жената от залата, която в първо действие води спора за Шагал и Бели Искър.
Тя е много нервна.

ЖЕНА-СИМВОЛ. Това ли ще гледаме сега?… Тези ли примитиви?… Защо не започва
пиесата?… Изхвърлете ги най-после!… Ние сме дошли тук да гледаме театър!…
ДИРЕКТОР. Виждате, ситуацията е сложна…
ЖЕНА-СИМВОЛ. Не виждам какво му е толкова сложното. Изхвърляте ги тези… и
продължавате спектакъла. Стига вече простотии, стига вече балканщини. Къде е тук
животът на човешкия дух? Сега сме края на двайсети век!
КУМ (чак сега се ориентира). Ние ли сме „простотии“? Ние ли сме балканщини? Значи
народът, дето ви храни и облича, е „простотии“?… Като ви изкараме на пазара чушки и
Домати, не сме простотии, ама като дойдеме в театъра, сме простотии, така ли?
ДИРЕКТОР. Каратанасов, не влизай в пререкания! (Към Жената.) Моля ви, влезте ни в
положението!…
ЖЕНА-СИМВОЛ. Няма да вляза! Трийсет години вече само това правя – влизам някому в
положението!… Защо някой не влезе в моето положение!…
КУМ. Как да влезе, я се погледни каква си!… (Смее се.)
ДИРЕКТОР. Каратанасов!…
ЖЕНА-СИМВОЛ. Кога ще престанете най-после с тази простащина! Театърът е
държавен!…
КУМ. А държавата на кого е? На народа!
ЖЕНА-СИМВОЛ. Ще прекратите ли най-после този кич или не?
КУМ. Ти си кич!…
ДИРЕКТОР. Каратанасов!…
ЖЕНА-СИМВОЛ. И това ми било Сатиричен театър. Правилно ви отнеха званията.
Народни артисти, заслужили режисьори, деятели на културата!!!
ДИРЕКТОР. Каратанасов, защо влизаш в спор с публиката? Хората са прави, те са дошли
на театър. Не можем така да ги разгонваме.
КУМ. Ма тя обижда народа бе, началство, това преглъща ли се? Тебе да те нарекат кич, ти
как ще се чувстваш?…
ДИРЕКТОР. Ама ти защо се разправяш от сцената? Имаме достатъчно артисти, не сме
опрели до тебе!
ЖЕНА-СИМВОЛ. Халтураджии! (Излиза.)

КУМ. Началник, не се ядосвай! Тая не е добре. Е, аз се ядосах и огладнях. Ти не си ли
гладен?… Минчо, Минчо!… (Минчо се явява.) Я донеси за хапване, че ще повредим
гастрита така, гладни!…
МИНЧО. Окей!… (Излиза.)
Изведнъж се чуват два силни гърмежа. Връхлетява Цонка, последвана от изплашени
сватбари, които се изпокриват като пилци, който където свари. Цонка се скрива зад
Директора. Миг след това на сцената влиза с насочена пушка Георги. Всичко е залегнало и
изпокрито.
ГЕОРГИ (прави знак с ръка). Дръпни се!
ЦОНКА. Георги, недей!…
ГЕОРГИ. Дръпни се, щото стрелям!…
ДИРЕКТОР. Аз се предавам! (Вдига ръце.)
ЦОНКА. Гошо!
ГЕОРГИ. Млък! (На Директора.) Махай се, ти казвам, щото ще ви гръмна и двамата!…
ЦОНКА. Жоро!…
ГЕОРГИ. Млък!… Ти мълчи…
ЦОНКА. Жоро, ти не си такъв човек. Жоро, забрави ли, като ме целуна за първи път!…
Бяхме на седем години, Жоренце!… Ти беше с моряшка блузка!…
ГЕОРГИ. Не говори! (Прицелва се с пушката.)
ЦОНКА. А зидовете на училището забрави ли, и пионерския лагер, дето се заклехме във
вечна любов?…
ГЕОРГИ. Дума да не чуя повече! (На Директора.) Бягай оттам!
ЦОНКА. А Момина чешма, дето ловеше луната с ръце и ми я даваше? Нали не можеше да
живееш без мене, Георги? Нали ти бях първата и последната?
ГЕОРГИ. Млък, ти казвам! (Прицелва се.)
ЦОНКА. Защо ми даде тогава пръстенчето? И ми каза, че няма нищо да ни раздели?…
ГЕОРГИ. Стрелям!… (Прицелва се. Директорът вдига по-високо ръце.)

Внезапно Цонка излиза пред Директора и застава пред Георги.
ЦОНКА. Стреляй тогава!… (Пауза. Георги се е прицелил, но не стреля.) Стреляй, какво
чакаш!… (Георги приближава, Цонка запява тихо „Я кажи ми, облаче ле бяло…“ Запява и
Георги, после хвърля пушката и избягва от сцената. Цонка припада. Суматоха на сцената.)
ДИРЕКТОР. Лекар! Няма ли лекар!
КУМ. Не, баба Кера! Това е уплах! Викайте баба Кера!
ДИРЕКТОР. Ето!… Сега пък имам видения!… Какво е това?
КУМ. Кое!
ДИРЕКТОР. Е това! Което иде!…
КУМ. Ааа!… Баба Кера. Тя е от наша страна… от страната на булката. Гледачка. Бае
срещу уроки! Гледа на кафе… Познава всичко. Здравей, бабо Керо!…
БАБА КЕРА. Уплах! Донесете въглен и две барабонки от коза!
КУМ. Това е директорът на театъра.
БАБА КЕРА. Да го поживи Господ. Няма да е задълго.
КУМ. Хайде сега, бабо Керо, не плаши човека.
БАБА КЕРА. Той от наша страна ли е?
КУМ. Не, директор на театъра е, на това тук!… (Сочи.)
БАБА КЕРА. Значи откъм младоженека? Е, право съм рекла тогава. Ама да не се плаши,
може и да му се размине. Дечо, какво стана с барабонките?
ДИРЕКТОР. Какво е това на гърба ти, бабо Керо?
БАБА КЕРА. Вълна.
ДИРЕКТОР. А защо я носиш? Подарък ли?
БАБА КЕРА. Тя е четирсет килограма.
ДИРЕКТОР. А защо ти е, защо ти е?
БАБА КЕРА. Четирсет килограма е, рекох.
ДИРЕКТОР. Чух, чух, ама защо я носиш?

БАБА КЕРА. Чул си ти, ама не си разбрал. Нали туй ти казвам – четирсет килограма е.
ДИРЕКТОР. Добре де, ама какво ще я правиш?
БАБА КЕРА. Че какво могат да се правят четирсет килограма вълна? Ще ги дам за
„Отнесени от вихъра“.
ДИРЕКТОР. Че за какво ти е на тебе „Отнесени от вихъра“?
БАБА КЕРА. За образование.
ДИРЕКТОР. За какво образование?
БАБА КЕРА. Средно.
ДИРЕКТОР. Че ти тепърва да се образоваш ли си решила?
БАБА КЕРА. Това е за внука. Щото не може да мине в пети клас. Много учи, пък нищо не
знай.
ДИРЕКТОР. И какво ще научи от „Отнесени от вихъра“? Там тематиката е друга!…
БАБА КЕРА. Той нищо няма да научи, обаче сега тъй върви – тройка за „Отнесени от
вихъра“. В Търново можеш да вземеш и две, че и три тройки, ако се пазариш, щото там
окръг, повече четат.
ДИРЕКТОР. И ти му оправяш бележките?
БАБА КЕРА. Ми к’во да го правя? Обаче свърших овцете. Тази беше последната. (Влиза
Кумът с барабонките. Бабата бае, Цонка се свестява.)
ЦОНКА. Бате Дечо, разваляй сватбата, не мога, любов…
КУМ. Каква любов, то ако беше толкова хубаво, щяха да я продават в Кореком!… Дума да
не става!
ЦОНКА. Не мога! Георги!… Георги!… Ела бе, Георги!… (Излиза.)
КУМ. Началник, дай ключовете от кабинета да звънна по телефона на сватанака, че
работата стана дебела. (Излиза.)
ДИРЕКТОР. Бабо Керо, я да ми гледаш ти на мене, а? Бива ли?
БАБА КЕРА. На боб или на кафе?
ДИРЕКТОР. Все едно.

БАБА КЕРА. Не е то все едно.
ДИРЕКТОР. Добре де, каква е разликата? Боб, кафе…
БАБА КЕРА. Разликата… сега ще ти я кажа… (Вади „Елка“ и смята.) Седем долара и
петнайсет цента.
ДИРЕКТОР. Какво?!
БАБА КЕРА. Седем долара и петнайсет цента. Разликата.
ДИРЕКТОР. Чакай сега, как така?…
БАБА КЕРА. На боб е петдесет и два долара, на кафе – четирсет и четири долара и
осемдесет и пет цента. Остават – седем долара и петнайсет цента.
ДИРЕКТОР. Ама ти в долари ли взимаш?
БАБА КЕРА. Ако имаш марки, и то бива. Ама западни, вестдойч.
ДИРЕКТОР. Валута, искаш да кажеш?
БАБА КЕРА. Валута, баби, валута.
ДИРЕКТОР. Че как така валута? Откъде-накъде?
БАБА КЕРА. Материалите, баби, материалите са вносни – боб, кафе… все с валута…
Откъде иначе… Всичко се внася…
ДИРЕКТОР. Хайде сега, всичко… Ето, „Елка“ имаш!…
БАБА КЕРА. „Елка“ имам, ама боб нямам!…
ДИРЕКТОР. Е, всеки няма по нещо. Аз например нямам валута.
БАБА КЕРА. Като нямаш – няма да ти гледам.
ДИРЕКТОР. Бабооо, бабооо… Значи и ти валутен план гониш?
БАБА КЕРА. Такъв е животът, баби. (Звъни телефон.)
ДИРЕКТОР (на телефона). Ало? Да? Валутни афери! Суеверия? Хаос? Не, няма!…
Няма!… Да не се безпокоя повече? Защо? Защото съм уволнен? Да! Да! Разбрах. По
взаимно съгласие! Разбрах! Дочуване!… (Връща се.)
КУМ (влиза). Лоша работа, началник. Никой не се обажда

ДИРЕКТОР. Не съм вече началник, уволниха ме.
КУМ. Бре, че кога успяха? Телеграфически?
ДИРЕКТОР. Не, по телефона.
КУМ. Ами аз тогава отивам да търся новия. Тая работа не може да продължава така.
(Излиза.)
ДИРЕКТОР. Значи по 52 долара за едно гледане? Ми аз като директор получавах много
по-малко. Я да опитам и аз, а? Какво ще кажеш?
