Първо действие
Сумрачна зимна привечер. Студен, пронизващ вятър бръсне наоколо. Вали сняг, затрупва
Балкана. Притъмнява. В здрача се появяват двама души, те са с вдигнати яки на балтоните
и бомбета, нахлупени над очите. Ръцете им са в джобовете. Непознатите се оглеждат…
Чува се далечен вой на вълци…
ПЪРВИ. Вее…
ВТОРИ. Зиме в Балкана е така… (Пауза. Воят се повтаря, потретва.)
ПЪРВИ. Вият…
ВТОРИ. Изгладнели са… (Пауза.)
ПЪРВИ. Големи преспи…

ВТОРИ. За нас друг изход няма.
ПЪРВИ. Имаш ли огънче? (Вади цигара.)
ВТОРИ. На. (Подава му огънче, онзи запалва.)
ПЪРВИ. Дали е близо Бели Искър?
В този момент от салона, от третия ред, се разнася вик. Мъжете се обръщат и гледат
нататък…
ЖЕНА С ПРОБЛЕМ. Помощ! Крадец! Крадец!…
Тя е станала, държи съседа си за ръкава, той пък държи чантата ѝ в ръката си. Намесват се
двама граждани, които са редом и с викове „Насам! Дайте го насам! По-внимателно!…
Къде да го водим? Дай на сцената!…“ го извеждат от реда. Следачите се насочват натам,
настъпва суматоха, двамата непознати, смутени, изчезват от сцената. Там се качва цялата
група – крадецът, двамата, които здраво го държат, и жената. Сцената светва, спектакълът
се прекъсва.
ЖЕНА С ПРОБЛЕМ. Как не ви е срам!… Ние идваме тук да се обогатяваме, а вие какво –
по-бедни ще ни правите!… На какво прилича това?!?!
ПЪРВИ МЪЖ. Няма ли милиция тука?… Извикайте някой от театъра!
ВТОРИ МЪЖ. Директора!!!… Директора!… Има ли директор? Хей!…
ЖЕНА С ПРОБЛЕМ. Как не ви е срам да крадете в театър! Това да не ви е Централна гара!
Това е храм на изкуството!
ВТОРИ МЪЖ. Никакви храмове не базарят. Изпокрадоха ги. Икони, кадилници…
ПЪРВИ МЪЖ. Това е остатък от миналото.
ВТОРИ МЪЖ. Кадилниците ли?
ПЪРВИ МЪЖ. Кражбата.
ЖЕНА С ПРОБЛЕМ. Тъкмо съм се настроила естетически, завесата се отваря, а той ми
свива чантата.
ПЪРВИ МЪЖ. Директор!… Директораааа!… Има ли директор?…
ДИРЕКТОР. Какво става тук? Защо сте на сцената?
ЖЕНА С ПРОБЛЕМ. Този краде в поверения ви театър.

ДИРЕКТОР. Краде?!?!
ЖЕНА С ПРОБЛЕМ. Измъкна ми чантата. В културен институт!…
ПЪРВИ МЪЖ. Остатък от миналото!
ВТОРИ МЪЖ. Ние го държим.
ДИРЕКТОР. Как не ви е срам! Тука ли намерихте? Малко ли кина има?
БЯЛА ВРАНА. Кината нямат сцена.
ДИРЕКТОР. А вие какво? След като откраднете – излизате да се покланяте, така ли?
Очаквате да ви изкарат на бис?!…
БЯЛА ВРАНА. Очаквах да ме изкарат на сцената.
ДИРЕКТОР. И мога ли да зная защо?
БЯЛА ВРАНА. Защото ми омръзна да разговарям с ватмана. Ден, два, три, година, години
наред все говоря с ватмана.
ЖЕНА С ПРОБЛЕМ. Сега пък се прави на луд.
ВТОРИ МЪЖ. Стар номер.
ПЪРВИ МЪЖ. Това е остатък от миналото.
ДИРЕКТОР. Какъв ватман намесвате тука? Моля да не говорите за ватмани в поверения
ми театър. Защо крадете? Защо ви трябва сцена?
БЯЛА ВРАНА. Исках да поговоря с хората.
ДИРЕКТОР. С кои хора?
БЯЛА ВРАНА (сочи публиката). С тези. С моите съвременници.
ЖЕНА С ПРОБЛЕМ. Крадецът няма съвременници. Като си ми съвременник, защо ми
свиваш чантата?… Нали сме съвременници?
ДИРЕКТОР. Защо ти трябва да говориш с твоите съвременници?
БЯЛА ВРАНА. Омръзна ми все с ватмана. Години с ватмана.
ВТОРИ МЪЖ. А защо говориш с ватмана?
БЯЛА ВРАНА. Защото е забранено.

ЖЕНА С ПРОБЛЕМ. Пак се прави на луд.
БЯЛА ВРАНА. Не се правя. Нали сте виждали – във всички трамваи пише: „Забранено е да
се говори с ватмана!“.
ПЪРВИ МЪЖ. Това е факт – пише.
ДИРЕКТОР. А ти защо говориш, като е забранено?
БЯЛА ВРАНА. А какво да правя? Моите възможности са малки. Аз не мога да изпратя
сина си посланик само защото тукашното уиски се е влошило. Не мога да продавам
държавни служби. Не мога да раздам и сто декара гора, като уредя по този начин
фамилията си до девето коляно. Обаче и аз съм човек. И на мене ми се иска да направя
нещо, което е забранено от закона. За да мога да гледам децата си в очите.
ЖЕНА С ПРОБЛЕМ. И какво направи?
БЯЛА ВРАНА. Минах на червена светлина!
ВТОРИ МЪЖ. Дегенерат.
ПЪРВИ МЪЖ. Престъпник! Рецидивист!
БЯЛА ВРАНА. Но ме глобиха. Моментално. Изпратиха ме в неделно училище. Писаха
писмо в службата ми. Жигосаха ме в квартала. Окачиха снимката ми в училището, където
учат децата ми. Показаха ме в предаването „Лъч“… Оттогава говоря с ватмана. Точно под
табелката, на която пише, че говоренето с ватмана е забранено. Хем е забранено, хем е
напълно безопасно. И което е най-важното – напълно в моите възможности.
ДИРЕКТОР. Обаче ти омръзна и сега рече да поговориш със съвременниците си?
БЯЛА ВРАНА. Затова откраднах.
ДИРЕКТОР. Много съм любопитен какво ще им кажеш?
БЯЛА ВРАНА. Да почвам ли? (Директорът му прави знак. Към салона.) Уважаеми
съвременници! Признавам, аз не съм открил пеницилина. Аз съм съвсем обикновен човек.
Но имам проблем: не знам какво да говоря на децата си. Когато аз бях дете, на село още
говореха и помнеха, през четири поколения, как Митьо Белия откраднал три мамула. През
турско ги откраднал, а и внуците, и правнуците му не смееха да погледнат хората в очите.
Той беше бялата врана на рода си и на околията. Момите от неговия род даваха тройна
зестра и пак никой не ги искаше. А днес аз съм бялата врана на квартала. Живея от едната
заплата, не изчуквам автомобили, не вземам подкупи, в дюшека ми има парцали, а не
хилядарки. Само аз от целия блок си изтупвам дюшека, останалите въобще не ги изнасят.
Всички ме гледат накриво, приятелите ме напуснаха. Около мене има вакуум, защото не
съм лежал в затвора и в гаража ми няма две коли. Да не говорим, че въобще нямам гараж.

Нямам и скандали с жени, дори не съм се напивал като хората и не съм трошил прозорците
на половината блок. И в резултат на всичко това кварталната общественост ме сочи с
пръст и страни от мене като от чумав. Ето как се обърнаха нещата, уважаеми
съвременници. И ако мене кучетата ме яли, то кажете ми – какво да говоря на моите деца.
Как да ги възпитавам. В буквара пише едно, във вестниците друго, а в живота става трето
и четвърто. Малкият ми син постоянно иска да го водя на лекар. Лекар „уши-очи“. Няма
такъв лекар, му казвам, има „уши, нос, гърло“. Не, казва, трябва ми „уши-очи“, защото
едно чувам, а съвсем друго виждам. Детето мисли, че то е болно. Какво да му кажа,
уважаеми съвременници? Откъде да му намеря такъв лекар?… Ето, това ми е проблемът и
ще ви бъда много благодарен, ако ми помогнете да го реша.
ВТОРИ МЪЖ. Съвременници!
ДИРЕКТОР. Слушайте, тука е театър.
ВТОРИ МЪЖ. Че да не би само той да има проблеми. И аз имам.
ДИРЕКТОР. Сега театър ли ще правим, или ще поставяме проблеми?…
ВТОРИ МЪЖ. Ама откъде-накъде той може да говори със съвременниците си, а аз да не
мога? Той има проблеми, а ние нямаме, така ли?
ДИРЕКТОР. Вижте сега, това тука е театър, ако започнем да разговаряме със
съвременниците си, къде ще му излезе краят?
ВТОРИ МЪЖ. Знам го аз вашия театър! Непрекъснато средновековие и принцове! И то –
чужди!… Нямаме си ние наши принцове, ами ще гледаме чужди!…
ДИРЕКТОР. С неспециалисти за театър не говоря. Имаме си ги достатъчно и в театъра.
Напуснете сцената!…
ЖЕНА С ПРОБЛЕМ. Ама защо не му дадете да каже. Той все пак ни помогна… Ако не
беше той…
ПЪРВИ МЪЖ. И Вазов казва същото.
ДИРЕКТОР. Какво казва Вазов? Какво казва Вазов?
ПЪРВИ МЪЖ. За моралните стимули. Като няма поету награда – вдъхновенье чезне,
фантазия отпада…
ДИРЕКТОР. Той да не е поет?
ВТОРИ МЪЖ. Метеоролог съм.
ДИРЕКТОР. Виждате ли – метеоролог!… Каква награда? Какво вдъхновенье? Спрете този
сняг! Ще ни затрупа!

ВТОРИ МЪЖ. Само две думи. Моля ви!…
ДИРЕКТОР. Две може. Едносрични.
ВТОРИ МЪЖ. Съвременници!…
ДИРЕКТОР. По-кратко!…
ВТОРИ МЪЖ. Добре. (Към публиката.) Вие нас на маймуни ни обърнахте.
ДИРЕКТОР. Кои вас?
ВТОРИ МЪЖ. Метеоролозите.
ДИРЕКТОР. Ами така го кажете. Какво обобщавате!…
ВТОРИ МЪЖ. Вие нас, метеоролозите, на маймуни ни обърнахте. Ние предсказваме
времето. Определяме климата. Сега навсякъде за климат се говори. Обаче какъв климат?
Никакъв климат. Пълно объркване и ненаучност!…
ДИРЕКТОР. Ама какво пак обобщавате!… Поетите не обобщават, метеоролозите
обобщават!… Кажете го кратко и ясно!…
ВТОРИ МЪЖ. Добре. (Към публиката.) Много вятър правите, съвременници мои! Много
вятър!… Оформим ние баричните полета, анализираме спътниковите снимки, дадем
научнообоснована прогноза и изведнъж, ни в клин, ни в ръкав над страната се появява
вятър. Над целия европейски континент тихо, а над страната – вятър. Ненаучен, нелогичен,
небазиран на нищо вятър. Този прави вятър на онзи, вторият – на третия, третият – на
четвъртия, едни вихри и талази заливат (Поглежда към Директора.)… отделни райони…
Всякаква прогноза отива по дяволите. Това вече не е професия! Какъв климат,
съвременници мои, вече не знаем какво да правим, за смях станахме…
ДИРЕКТОР. Край! Край! Край!… Никакви проблеми повече! Тук не е конгрес на
метеоролозите!… Моля ви! Много ви моля!… Напуснете! (Избутва Бялата врана и
Метеоролога навън.) И вие също!… Навън!… Напуснете!… Моля!…
ПЪРВИ МЪЖ (съпротивлява се). Знаете ли какво казва Вазов?…
ДИРЕКТОР. Не знам и не желая да знам!… Какво ви е казал Вазов, си е ваша работа!…
Напуснете!…
ЖЕНА С ПРОБЛЕМ. Нали има гласност? Защо ни запушвате устата?
ДИРЕКТОР. Гласност не значи, който има проблем, да се качи на сцената и да си го каже.
Моля… (Гони ги.)

ПЪРВИ МЪЖ. Но ние също имаме проблеми… не е справедливо… Едни могат, а други не
могат, така ли? Нима човек трябва да открадне, за да…
ДИРЕКТОР. Вие какво? Искате и аз да имам проблеми, така ли? (В този момент звъни
телефон, поставен на специална масичка отпред.) Ето!… Вече ги имам!… (Обажда се.)
Ало!… Слушам! Да!… Да, има, директор има, аз съм, тук съм… Не, никаква бъркотия,
сега започваме, след секунда… Да, ще ръководим… не, неуправляемо не, всичко ще бъде
управляемо… Но по-внимателно?… Разбирам… Сега вее друг вятър, да… разбирам!…
Слушам!… Дочуване!… (Оставя слушалката и много усмихнат, към Първи мъж.)
Заповядайте, кажете си проблема, много моля!… Много моля!… За нас е удоволствие!…
И така?…
ПЪРВИ МЪЖ. Съвременници!… Ученици мои!…
ДИРЕКТОР. Чакайте, какви ученици сега?
ПЪРВИ МЪЖ. Аз цял живот пиша учебници… автор съм на задачи по математика, на
онези за басейните… Нали се сещате – един басейн се пълни от една тръба за два часа…
ДИРЕКТОР. Ааа, вие ли сте този човек?! Знаете ли, че бяхте най-псуваният мъж в нашето
училище?
ПЪРВИ МЪЖ. Знам. Мога ли да продължа?
ДИРЕКТОР. Моля!
ПЪРВИ МЪЖ. Съвременници! Ученици мои!… Ако мислите, че за трийсет години съм ви
дал нещо, макар и малко, помогнете ми сега и вие. Намерете ми пропуск за басейн!…
ДИРЕКТОР. За какъв басейн?
ПЪРВИ МЪЖ. За плувен.
ДИРЕКТОР. Това ли ви е проблемът?
ПЪРВИ МЪЖ. Не! Проблемът е в друго. Когато започвах, в цяла България имаше един
басейн, в Централната баня. Но той беше от друг тип. Пълнеше се и се празнеше през
тръби. Поколения след поколения решаваха задачите с басейни, докато преди два месеца
един четвъртокласник ми заяви, че това е невъзможно.
ДИРЕКТОР. Кое?
ПЪРВИ МЪЖ. Един басейн, който събира 600 хектолитра вода, да се пълни през една
тръба за два часа, а да се изпразва през две тръби за половината от синуса на котангенса на
числото пи, повдигнато на квадрат.
ДИРЕКТОР. Аз също мисля, че е невъзможно. На тая задача ме скъсаха на матурата.

ПЪРВИ МЪЖ. Теоретично всичко е вярно. Няма грешка. Но ученикът ми заяви, че сега
басейните въобще не се пълнят през тръби, а по съвсем друга система. „А ти реши
задачата – му казах, – а как се пълнят басейните, е друг въпрос“. Той обаче каза, че
задачата на училището е преди всичко да подпомага практиката. И ако не съм чел най-
новите документи по този въпрос, не било лошо да ги прочета. Тук аз кипнах и му казах,
че нашата цел на тази възраст не е да пълним и празним басейни, а да се научим чисто
теоретично да решаваме задачи. На което той отговори, че чисто теоретично можем да
изчислим и колко ангели могат да кацнат на върха на една игла, но какво ще помогне това
да надминем Япония в следващите петнайсет години?
ДИРЕКТОР. Ние не бяхме толкова умни. Като не можехме да решим задачата, си
отнасяхме двойката, и толкоз.
ПЪРВИ МЪЖ. Аз също щях да му я пиша. Но директорът каза, че не е лошо да изуча един
съвременен басейн, да преустроим малко работата и в математиката, навсякъде ставала
„перестройка“. Обаче, за да се преустрои математиката, трябва да се преустроят тези от
басейните. За да се преустроят басейните, трябва да се преустрои топлофикацията. За да се
преустрои топлофикацията, трябва да се преустрои механизацията. За да се преустрои
